// ─── Фильтр галлюцинаций в пунктах списка ────────────────────────────────────
const HALLUCINATION_PATTERNS = [
  /романтическая комедия о/i,
  /триллер про/i,
  /история о/i,
  /фильм про/i,
  /сериал про/i,
  /комедия про/i,
  /драма про/i,
  /история про/i,
  /семейный фильм о/i,
  /анимационный фильм о/i,
];

function filterHallucinatedLines(text) {
  const lines = text.split("\n");
  const filtered = lines.filter((line) => {
    // Проверяем только пункты списка (начинаются с цифры и точки)
    if (!/^\d+\.\s/.test(line)) return true;
    // Если пункт содержит паттерн галлюцинации — удаляем
    return !HALLUCINATION_PATTERNS.some((pattern) => pattern.test(line));
  });
  // Перенумеровываем оставшиеся пункты
  let counter = 1;
  return filtered
    .map((line) => {
      if (/^\d+\.\s/.test(line)) {
        return line.replace(/^\d+\./, `${counter++}.`);
      }
      return line;
    })
    .join("\n");
}

// ─── Handler ─────────────────────────────────────────────────────────────────
export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    return res.status(500).json({ error: "API key not configured" });
  }

  try {
    const { prompt, maxTokens = 2000, email } = req.body;

    if (!prompt) return res.status(400).json({ error: "prompt is required" });
    if (!email)  return res.status(400).json({ error: "email is required" });

    const normalizedEmail = email.toLowerCase().trim();
    const isAdmin = ADMIN_EMAILS.includes(normalizedEmail);
    const usageKey = `usage:${normalizedEmail}`;

    // Проверка лимита
    if (!isAdmin) {
      const used = Number((await redis.get(usageKey)) ?? 0);
      if (used >= FREE_LIMIT) {
        return res.status(429).json({
          error: "free_limit_reached",
          message: "Вы использовали все 3 бесплатных поста. Для продолжения оформите подписку.",
          used,
          limit: FREE_LIMIT,
          remaining: 0,
          upgradeRequired: true,
        });
      }
    }

    const callClaude = async (system, messages, tools = [], maxTok = 1000) => {
      const r = await fetch("https://api.anthropic.com/v1/messages", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "x-api-key": apiKey,
          "anthropic-version": "2023-06-01",
        },
        body: JSON.stringify({
          model: "claude-haiku-4-5-20251001",
          max_tokens: maxTok,
          system,
          ...(tools.length ? { tools } : {}),
          messages,
        }),
      });
      if (!r.ok) {
        const err = await r.json().catch(() => ({}));
        if (r.status === 529 || err.error?.type === "overloaded_error") {
          throw new Error("overloaded");
        }
        throw new Error(err.error?.message ?? `HTTP ${r.status}`);
      }
      return r.json();
    };

    // ── ЭТАП 1: Исследование — только реальные данные в JSON ──────────────────
    const researchPrompt = `Найди реальные данные для поста.\n\n${prompt}\n\nВерни только JSON без markdown-обёрток.`;

    const researchData = await callClaude(
      RESEARCH_SYSTEM,
      [{ role: "user", content: researchPrompt }],
      [{ type: "web_search_20250305", name: "web_search", max_uses: 3 }],
      1000
    );

    // Извлекаем JSON из ответа этапа 1
    const researchRaw = researchData.content
      .filter((b) => b.type === "text")
      .map((b) => b.text)
      .join("\n")
      .trim();

    let researchJson = { items: [], context: "" };
    try {
      const clean = researchRaw.replace(/```json|```/g, "").trim();
      researchJson = JSON.parse(clean);
    } catch {
      // Если JSON не распарсился — продолжаем с пустым, пост будет без списка
      console.warn("Research JSON parse failed:", researchRaw.slice(0, 200));
    }

    // Если данных нет вообще — возвращаем ошибку
    if (!researchJson.items || researchJson.items.length === 0) {
      // Fallback: пробуем написать пост без конкретного списка (для форматов не-топ)
      researchJson = { items: [], context: "Данные не найдены. Пиши пост на основе общих знаний по теме без списка конкретных названий." };
    }

    // ── ЭТАП 2: Написание поста на основе реальных данных ─────────────────────
    const writingPrompt = [
      prompt,
      "",
      "ДАННЫЕ ДЛЯ ПОСТА (только эти данные использовать):",
      "```json",
      JSON.stringify(researchJson, null, 2),
      "```",
      "",
      "Напиши 2 поста используя ТОЛЬКО названия и факты из JSON выше.",
      "Начни сразу с ===ПОСТ 1===",
    ].join("\n");

    const writingData = await callClaude(
      WRITING_SYSTEM,
      [{ role: "user", content: writingPrompt }],
      [],
      maxTokens
    );

    const rawText = writingData.content
      .filter((b) => b.type === "text")
      .map((b) => b.text)
      .join("\n")
      .trim();

    if (!rawText) {
      return res.status(500).json({ error: "Модель не вернула текст. Попробуйте снова." });
    }

    // Убираем текст до первого маркера
    const markerIndex = rawText.search(/={2,}ПОСТ\s*\d+={2,}/i);
    const stripped = markerIndex > 0 ? rawText.slice(markerIndex) : rawText;

    // Обрезаем после ===ПОСТ 2===
    const post3Match = stripped.search(/={2,}ПОСТ\s*3={2,}/i);
    const trimmedToTwo = post3Match > 0 ? stripped.slice(0, post3Match).trim() : stripped;

    // Постобработка
    let text = trimmedToTwo;

    // 1. Фильтр галлюцинаций в пунктах списка
    text = filterHallucinatedLines(text);

    // 2. Склеиваем разорванные пункты списка (название на одной строке, описание на следующей)
    const lines = text.split("\n");
    const joined = [];
    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      const next = lines[i + 1];
      if (
        next !== undefined &&
        /^\d+\.\s/.test(line) &&
        /\s—\s*$/.test(line) &&
        !/^\d+\.\s/.test(next.trim()) &&
        next.trim() !== ""
      ) {
        joined.push(line.trimEnd() + " " + next.trim());
        i++;
      } else {
        joined.push(line);
      }
    }
    text = joined.join("\n");

    // 3. Убираем одиночные знаки препинания
    text = text
      .replace(/\n([.,;:!?])\s*/g, "$1 ")
      .replace(/^\s*[.,;:!?]\s*$/gm, "");

    // 4. Без пустой строки перед пунктами списка
    text = text.replace(/\n\n(\d+\.)/g, "\n$1");

    // 5. Максимум два переноса подряд
    text = text.replace(/\n{3,}/g, "\n\n").trim();

    // Счётчик
    let newCount = FREE_LIMIT;
    if (!isAdmin) {
      newCount = await redis.incr(usageKey);
    }

    const remaining = isAdmin ? 999 : Math.max(0, FREE_LIMIT - newCount);

    return res.status(200).json({
      text,
      usage: {
        used:      isAdmin ? 0 : newCount,
        limit:     isAdmin ? 999 : FREE_LIMIT,
        remaining,
        warning:
          !isAdmin && remaining === 1
            ? "Остался 1 бесплатный пост. После этого потребуется подписка."
            : !isAdmin && remaining === 0
            ? "Это был ваш последний бесплатный пост. Оформите подписку для продолжения."
            : null,
      },
    });
  } catch (err) {
    if (err.message === "overloaded") {
      return res.status(503).json({ error: "Сервис временно перегружен. Попробуйте через минуту." });
    }
    console.error("generate error:", err.message, err.stack);
    return res.status(500).json({ error: err.message });
  }
}
